    private void LIST_TEMPLATESMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LIST_TEMPLATESMousePressed
        if (evt.getClickCount()==2) {
            TEMPLATE_EDITOR.setText(PropertiesReader.getFileAsString("templates"+RTProperties.FS+LIST_TEMPLATES.getSelectedValue()));
            TEMPLATE_NAME.setText(LIST_TEMPLATES.getSelectedValue());
            TEMPLATE_CURRENT=LIST_TEMPLATES.getSelectedValue();
        }
    }//GEN-LAST:event_LIST_TEMPLATESMousePressed

    private void BTN_NEWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_NEWActionPerformed
        BTN_SAVEActionPerformed(evt);
        TEMPLATE_NAME.setText("");
        TEMPLATE_EDITOR.setText("");
    }//GEN-LAST:event_BTN_NEWActionPerformed

    private void BTN_SAVEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_SAVEActionPerformed
        if (TEMPLATE_NAME.getText().length()>0) {
            PropertiesReader.savePropertiesFile("templates"+RTProperties.FS+TEMPLATE_CURRENT, TEMPLATE_EDITOR.getText());
            if (TEMPLATE_CURRENT.equalsIgnoreCase(LIST_TEMPLATES.getSelectedValue()) &&
                    !TEMPLATE_CURRENT.equalsIgnoreCase(TEMPLATE_NAME.getText())) {
                PropertiesReader.renameFile("templates"+RTProperties.FS+TEMPLATE_CURRENT, "templates"+RTProperties.FS+TEMPLATE_NAME.getText());
                
            }
        }
        LIST_TEMPLATES.setModel(PropertiesReader.getFilesAsListModel("templates"+RTProperties.FS));
        
    }//GEN-LAST:event_BTN_SAVEActionPerformed

