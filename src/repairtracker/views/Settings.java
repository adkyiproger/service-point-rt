    @Override
    public void save(){
    LOGGER.info("Save pressed");
    
        RepairTracker.PROPERTIES.setProperty("LOG_FILE", LOG_FILE.getText());
        
        
        String lf_name = CB_THEME.getSelectedItem().toString();
        lf_name=lf_name.replaceAll("[^\\p{Alpha}\\p{Digit}]+","");
        System.out.println("CB_THEME: "+lf_name);
        for (Map.Entry pairs : LOOK_AND_FEEL.entrySet()) {
            if (pairs.getKey().toString().replaceAll("[^\\p{Alpha}\\p{Digit}]+","").matches(lf_name)) {
                RepairTracker.PROPERTIES.setProperty("THEME", pairs.getKey().toString());
                RepairTracker.PROPERTIES.setProperty("THEME_CLASS", pairs.getValue().toString());
                System.out.println("Matched");
            }
        }
        switch (CB_LANGUAGE.getSelectedIndex()) {
            case 1:
                RepairTracker.PROPERTIES.setProperty("LOCALE_CODE", "ru_RU");
                break;
            case 2:
                RepairTracker.PROPERTIES.setProperty("LOCALE_CODE", "en_US");
                break;
            default:
                RepairTracker.PROPERTIES.setProperty("LOCALE_CODE", "uk_UA");
                break;
        }
        
        
        RepairTracker.PROPERTIES.setProperty("EDITOR_FONT_FAMILY",CB_FONT_FAMILY.getSelectedItem().toString());
        RepairTracker.PROPERTIES.setProperty("EDITOR_FONT_SIZE",CB_FONT_SIZE.getSelectedItem().toString());
        RepairTracker.PROPERTIES.setProperty("LIST_FONT_FAMILY",CB_FONT_FAMILY1.getSelectedItem().toString());
        RepairTracker.PROPERTIES.setProperty("LIST_FONT_SIZE",CB_FONT_SIZE1.getSelectedItem().toString());
        RepairTracker.PROPERTIES.setProperty("PRINT_FONT_FAMILY",CB_FONT_FAMILY2.getSelectedItem().toString());
        RepairTracker.PROPERTIES.setProperty("PRINT_FONT_SIZE",CB_FONT_SIZE2.getSelectedItem().toString());

        RepairTracker.PROPERTIES.setProperty("PRINTER_PAGE", PRINTER_PAGE.getSelectedItem().toString());
        RepairTracker.PROPERTIES.setProperty("PRINTER_ORIENTATION", PRINTER_ORIENTATION.getSelectedItem().toString());
        RepairTracker.PROPERTIES.setProperty("PRINTER_BORDER_TOP", String.valueOf(BORDER_TOP.getValue()));
        RepairTracker.PROPERTIES.setProperty("PRINTER_BORDER_LEFT", String.valueOf(BORDER_LEFT.getValue()));
        RepairTracker.PROPERTIES.setProperty("PRINTER_BORDER_RIGHT", String.valueOf(BORDER_RIGHT.getValue()));
        RepairTracker.PROPERTIES.setProperty("PRINTER_BORDER_BUTTOM", String.valueOf(BORDER_BUTTOM.getValue()));
        
        RTProperties.SaveProperties(RepairTracker.PROPERTIES);
        RepairTracker.PROPERTIES=RTProperties.ReadProperties();
    }
    
