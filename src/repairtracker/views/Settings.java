     private DefaultComboBoxModel<String> getFontModel(){
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        
        for (String l : GraphicsEnvironment.getLocalGraphicsEnvironment()
        .getAvailableFontFamilyNames()) model.addElement(l);
       return model;
    }
    
    private DefaultComboBoxModel<Integer> getFontSizeModel(){
        DefaultComboBoxModel<Integer> model = new DefaultComboBoxModel<>();
        for (int ii=12;ii<30;ii++) model.addElement(ii);
       return model;
    }
    
